'''
Loop with While
'''
count = 0
while(count < 5):
    print(count)
    count+=1
else:
    print("Loop finalizado!")

'''
Loop with for
'''
for letra in 'texto':
    print(letra)
else:
    print("Fim do loop for")

for numero in range(11):
    print(numero+1)
else:
    print("Fim loop for")